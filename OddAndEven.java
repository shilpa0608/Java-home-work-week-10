package com.learning.java;
public class OddAndEven {
    public static void main(String[] args) {
        int i,b;
        for (i = 1; i <20; i = i+2) {
            System.out.println("odd number" +i);
            for (b = 0; b<20; b = b+2) {
                System.out.println("even number" +b);
        }

        }

    }
    }


