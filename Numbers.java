package com.learning.java;

public class Numbers {
    public static void main(String[] args) {
        int i = 1;
        int j = 2;
        while (i <20) {
            i += 2;
            System.out.println(i); //odd numbers//
        }
        while (j <20) {
            j += 2;
            System.out.println(j);//even numbers//

        }
    }
}





